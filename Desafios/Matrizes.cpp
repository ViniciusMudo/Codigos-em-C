#include <stdio.h>
#include <locale.h>
int main(){
    setlocale(LC_ALL, "Portuguese");
    int mB[20][20], n=0, i, j, a, b, soma;
    float mA[5][5], media;
    printf("                                                                       & MATRIZES &                                                                            \n\n");
	for(i=0;i<20;i++){
    	for(j=0;j<20;j++){
    		mB[i][j]=1+n;
    		n++;
		}
	}
	printf("|#########_________________________________________________________\\MATRIZ 'B' (20x20)/__________________________________________________________#########|\n\n");
	for(i=0;i<20;i++){
        for(j=0;j<20;j++){
			printf("%d\t", mB[i][j]);
	    }
	    printf("\n\n");
	}
	for(i=0;i<5;i++){
		for(j=0;j<5;j++){
		    soma=0;	
			for(a=4*i;a<=4*i+3;a++){				
				for(b=4*j;b<=4*j+3;b++){
					soma=soma+mB[a][b];
				}
			}
			media=(float)soma/16;
			mA[i][j]=media;
		}
	}
	printf("|_________________________________________________________________________________________________________________________________________________________|\n");
	printf("\n                |####_____________________\\MATRIZ 'A' (5x5)/_____________________####|\n\n");
	for(i=0;i<5;i++){
		for(j=0;j<5;j++){
			printf("\t\t%.2f ", mA[i][j]);
		}
		printf("\n\n");
    }
    printf("\n                |____________________________________________________________________|\n\n");
	return 0;
}
